<template>
  <div class="max-w-2xl mx-auto xl:w-96 w-auto">
    <form class="flex items-center" @submit.prevent="onSubmit">
      <label for="simple-search" class="sr-only">Search</label>
      <div class="relative w-full">
        <div class="flex absolute inset-y-0 left-0 items-center pl-3 pointer-events-none">
          <svg
            class="w-5 h-5 text-gray-500 dark:text-gray-400"
            fill="currentColor"
            viewBox="0 0 20 20"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path
              fill-rule="evenodd"
              d="M8 4a4 4 0 100 8 4 4 0 000-8zM2 8a6 6 0 1110.89 3.476l4.817 4.817a1 1 0 01-1.414 1.414l-4.816-4.816A6 6 0 012 8z"
              clip-rule="evenodd"
            ></path>
          </svg>
        </div>

        <input
          v-model="searchQuery"
          type="text"
          id="simple-search"
          class="bg-transparent border border-sky-900 text-gray-900 dark:text-white text-sm rounded-lg block w-full pl-10 p-2.5"
          placeholder="Buscar recetas"
          required
        />
      </div>
    </form>
  </div>
</template>

<script lang="ts" setup>
import { ref } from 'vue'
import { useRouter } from 'vue-router'

const router = useRouter()
const searchQuery = ref<string>()

const onSubmit = (): void => {
  router.push({ name: 'search', query: { q: searchQuery.value } })

  searchQuery.value = ''
}
</script>
